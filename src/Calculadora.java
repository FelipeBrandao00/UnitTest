public class Calculadora {
    public  int soma(int num1,int num2){
        return num1 + num2;
    }
    public double divisao(int num1,int num2){
        return ((double) num1 /num2);
    }
    public double produto(int num1,int num2){
        return num1 * num2;
    }
    public double diferenca(int num1,int num2){
       return num1 - num2;
    }
}
